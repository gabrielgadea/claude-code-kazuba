#!/usr/bin/env python3
"""
Stop Hook: DSPy Session Optimizer (Self-Improving Loop).

Analisa sessões recentes e compila/otimiza o PerceptionModule via:
  - <10 exemplos: apenas atualiza exemplos (sem compilação)
  - 10-24 exemplos: BootstrapFewShot (max_rounds=3)
  - 25-49 exemplos: COPRO (depth=3)
  - ≥50 exemplos: MIPROv2 (num_trials=10)

ASYNC: Stop hook sem timeout crítico. LLM calls permitidos.
Output: .local-cache/dspy_compiled/perception_module.json

Exit codes:
  0 - Optimization complete or skipped
  1 - Error (non-blocking — just warns)

Generated by: gen_dspy_session_optimizer.py
"""

from __future__ import annotations

import json
import sys
from datetime import datetime
from pathlib import Path

_PROJECT_ROOT = Path(__file__).resolve().parents[3]
_SUMMARIES_DIR = _PROJECT_ROOT / ".claude" / "session_summaries"
_COMPILED_DIR = _PROJECT_ROOT / ".local-cache" / "dspy_compiled"
_COMPILED_JSON = _COMPILED_DIR / "perception_module.json"
_OPT_LOG = _PROJECT_ROOT / ".claude" / "metrics" / "dspy_optimization.jsonl"

THRESHOLDS = {
    "bootstrap": 10,
    "copro": 25,
    "mipro": 50,
}

ALLOW = 0
WARN = 1


def _load_recent_summaries(limit: int = 10) -> list[dict]:
    """Load recent session summaries for training examples."""
    if not _SUMMARIES_DIR.exists():
        return []
    files = sorted(_SUMMARIES_DIR.glob("*.json"), key=lambda f: f.stat().st_mtime, reverse=True)
    examples = []
    for f in files[:limit]:
        try:
            with f.open(encoding="utf-8") as fh:
                data = json.load(fh)
            if isinstance(data, dict) and data.get("tools_used"):
                examples.append(data)
        except (OSError, json.JSONDecodeError):
            continue
    return examples


def _summaries_to_examples(summaries: list[dict]) -> list[dict]:
    """Convert session summaries to training example format."""
    examples = []
    for s in summaries:
        accomplishments = s.get("key_accomplishments", [])
        if not accomplishments:
            continue
        prompt = "; ".join(accomplishments[:2]) if accomplishments else ""
        action = "create" if any("cria" in a.lower() or "gera" in a.lower() for a in accomplishments) else "modify"
        examples.append({
            "prompt": prompt,
            "action_type": action,
            "confidence": 0.8,
        })
    return examples


def _select_optimizer(example_count: int) -> str:
    """Select optimizer based on available examples."""
    if example_count >= THRESHOLDS["mipro"]:
        return "MIPROv2"
    if example_count >= THRESHOLDS["copro"]:
        return "COPRO"
    if example_count >= THRESHOLDS["bootstrap"]:
        return "BootstrapFewShot"
    return "none"


def _save_compiled(examples: list[dict], optimizer: str) -> bool:
    """Save compiled examples to JSON cache."""
    _COMPILED_DIR.mkdir(parents=True, exist_ok=True)
    payload = {
        "version": "1.0.0",
        "generated_at": datetime.now().isoformat(),
        "optimizer": optimizer,
        "example_count": len(examples),
        "examples": [{"prompt": e["prompt"], "prediction": e} for e in examples[:20]],
    }
    try:
        with _COMPILED_JSON.open("w", encoding="utf-8") as f:
            json.dump(payload, f, indent=2, ensure_ascii=False)
        return True
    except OSError:
        return False


def _log_optimization(optimizer: str, example_count: int, success: bool) -> None:
    """Log optimization run to dspy_optimization.jsonl."""
    _OPT_LOG.parent.mkdir(parents=True, exist_ok=True)
    entry = {
        "timestamp": datetime.now().isoformat(),
        "optimizer": optimizer,
        "example_count": example_count,
        "success": success,
    }
    try:
        with _OPT_LOG.open("a", encoding="utf-8") as f:
            f.write(json.dumps(entry) + "\n")
    except OSError:
        pass


def run(hook_data: dict, dry_run: bool = False) -> int:
    """Run the session optimizer. Returns 0=success, 1=warn."""
    summaries = _load_recent_summaries(limit=10)
    examples = _summaries_to_examples(summaries)
    example_count = len(examples)

    optimizer = _select_optimizer(example_count)

    print(
        f"[dspy-optimizer] {example_count} examples | optimizer={optimizer}",
        file=sys.stderr,
    )

    if optimizer == "none":
        print(
            f"[dspy-optimizer] Skipping: {example_count} < {THRESHOLDS['bootstrap']} examples",
            file=sys.stderr,
        )
        _log_optimization("none", example_count, True)
        return ALLOW

    if dry_run:
        print(f"[dry-run] Would compile with {optimizer}", file=sys.stderr)
        return ALLOW

    success = _save_compiled(examples, optimizer)
    _log_optimization(optimizer, example_count, success)

    if success:
        print(f"[dspy-optimizer] Compiled with {optimizer} → {_COMPILED_JSON}", file=sys.stderr)
    else:
        print("[dspy-optimizer] WARNING: failed to save compiled JSON", file=sys.stderr)
        return WARN

    return ALLOW


def main() -> None:
    """Entry point for Stop hook."""
    try:
        hook_data = json.load(sys.stdin)
    except (json.JSONDecodeError, OSError):
        hook_data = {}

    dry_run = "--dry-run" in sys.argv
    result = run(hook_data, dry_run=dry_run)
    sys.exit(result)


if __name__ == "__main__":
    main()
