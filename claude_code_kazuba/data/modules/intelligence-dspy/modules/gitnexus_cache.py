        data = cls._load_cache()
        if data is None:
            return "[GitNexus cache miss: run 'python scripts/aco/generators/gen_gitnexus_hints.py']"
        if cls._is_expired(data):
            return "[GitNexus cache stale: TTL expired — run gen_gitnexus_hints.py]"
        return cls._select_relevant_hints(data, query)

