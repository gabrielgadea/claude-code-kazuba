from __future__ import annotations

import json
from datetime import datetime
from pathlib import Path
from typing import Any, ClassVar

_PROJECT_ROOT = Path(__file__).resolve().parents[3]
_CACHE_PATH = _PROJECT_ROOT / ".local-cache" / "gitnexus_hints.json"
_TTL_SECONDS = 3600


class GitNexusContextCache:
    """Cache-backed GitNexus hints reader. Never makes MCP calls at runtime."""

    _cache: ClassVar[dict[str, Any] | None] = None
    _cache_loaded_at: ClassVar[datetime | None] = None

