from __future__ import annotations

import json
import logging
from pathlib import Path
from typing import Any

import dspy

logger = logging.getLogger(__name__)

_PROJECT_ROOT = Path(__file__).resolve().parents[3]
_COMPILED_PATH = _PROJECT_ROOT / ".local-cache" / "dspy_compiled" / "perception_module.json"


def _load_compiled() -> dict[str, Any] | None:
    """Load compiled JSON if available. Returns None on miss."""
    if not _COMPILED_PATH.exists():
        return None
    try:
        return json.loads(_COMPILED_PATH.read_text(encoding="utf-8"))
    except (json.JSONDecodeError, OSError):
        return None


def _fallback_predict(prompt: str) -> dict[str, Any]:
    """Deterministic fallback when compiled JSON unavailable."""
    prompt_lower = prompt.lower()
    if any(w in prompt_lower for w in ["bug", "erro", "fix", "falha", "crash"]):
        action = "debug"
        arch = "Reflexion"
    elif any(w in prompt_lower for w in ["refactor", "refatora", "reorgani"]):
        action = "refactor"
        arch = "ToT"
    elif any(w in prompt_lower for w in ["cri", "crea", "impl", "add", "adi"]):
        action = "create"
        arch = "GoT" if len(prompt) > 300 else "CoT"
    elif any(w in prompt_lower for w in ["explica", "explain", "o que", "como"]):
        action = "explain"
        arch = "CoT"
    elif any(w in prompt_lower for w in ["analisa", "analis", "analiz", "revis", "audit"]):
        action = "analyze"
        arch = "CoT"
    else:
        action = "modify"
        arch = "CoT"
    return {
        "action_type": action,
        "cognitive_architecture": arch,
        "task_tree": f"root > análise > {action} > verificação",
        "independent_tasks": [f"{action}_task"],
        "sequential_tasks": ["análise", action, "validação"],
        "active_constraints": ["CODE-FIRST", "ruff-0", "CC≤10"],
        "success_criteria": f"Tarefa {action} executada com sucesso e validada",
        "confidence": 0.7,
    }


