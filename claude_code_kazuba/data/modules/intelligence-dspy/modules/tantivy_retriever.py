"""TantivyRetriever — DSPy Retrieve backed by scripts/discover.py (Tantivy 3-tier).

DESIGN: Usa subprocess para discover.py (não import direto) porque:
1. discover.py tem 3-tier fallback próprio (Tantivy→ripgrep→rglob)
2. Evita overhead de import + inicialização de índice no hook SYNC
3. Padrão CODE-FIRST: subprocess é o contrato público de discover.py

Generated by: gen_tantivy_retriever.py
"""

from __future__ import annotations

import json
import subprocess
from pathlib import Path

import dspy

_PROJECT_ROOT = Path(__file__).resolve().parents[3]
_DISCOVER_PY = _PROJECT_ROOT / "scripts" / "discover.py"
_VENV_PY = _PROJECT_ROOT / ".venv" / "bin" / "python"


def _parse_discover_output(stdout: str) -> list[str]:
    """Parse discover.py JSON output into passages list.

    discover.py --json returns {"query":..., "tier_used":..., "results":[{...}]}
    Each result has: path, score, doc_type, module
    """
    if not stdout.strip():
        return []
    try:
        data = json.loads(stdout)
        # discover.py --json returns a dict with "results" key
        if isinstance(data, dict):
            results = data.get("results", [])
            passages = []
            for r in results:
                if isinstance(r, dict) and r.get("path"):
                    score = r.get("score", 0.0)
                    module = r.get("module", "")
                    path = r["path"]
                    suffix = f" [{module}]" if module else ""
                    passages.append(f"{path}{suffix} [score={score:.2f}]")
            return passages
        # Legacy list format (fallback)
        if isinstance(data, list):
            passages = []
            for item in data:
                if isinstance(item, dict):
                    name = item.get("name", "")
                    desc = item.get("description", item.get("content", ""))
                    path = item.get("source_file", item.get("path", ""))
                    passages.append(f"{name} ({path}): {desc}"[:400])
                elif isinstance(item, str):
                    passages.append(item[:400])
            return passages
    except (json.JSONDecodeError, KeyError):
        pass
    # Fallback: plain text lines
    return [line.strip() for line in stdout.splitlines() if line.strip()][:10]


class TantivyRetriever(dspy.Retrieve):
    """DSPy Retriever backed by scripts/discover.py (Tantivy 3-tier).

    SYNC-safe: subprocess com timeout=3s. Fallback silencioso se discover falha.
    """

    def __init__(self, k: int = 3) -> None:
        super().__init__(k=k)

    def forward(self, query: str, k: int | None = None) -> dspy.Prediction:
        """Execute retrieval via discover.py subprocess."""
        limit = str(k or self.k)
        try:
            result = subprocess.run(
                [str(_VENV_PY), str(_DISCOVER_PY), query, "--limit", limit, "--json"],
                capture_output=True,
                text=True,
                timeout=3.0,
            )
            passages = _parse_discover_output(result.stdout)
        except (subprocess.TimeoutExpired, OSError):
            passages = []
        return dspy.Prediction(
            passages=passages or [f"[TantivyRetriever: no results for query: {query!r}]"]
        )


def get_retriever(k: int = 3) -> TantivyRetriever:
    """Factory: create TantivyRetriever instance."""
    return TantivyRetriever(k=k)
