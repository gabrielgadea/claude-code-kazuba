"""SerenaSymbolCache — Cache de contexto Serena via compliance.jsonl.

DESIGN: Parse do compliance.jsonl para extrair file/tool usage patterns.
Cache em memória por sessão (SHA256 do arquivo → resultado).
NÃO faz chamadas MCP Serena em runtime.

Generated by: gen_serena_cache.py
"""

from __future__ import annotations

import hashlib
import json
from pathlib import Path
from typing import Any, ClassVar

_PROJECT_ROOT = Path(__file__).resolve().parents[3]
_COMPLIANCE_PATH = _PROJECT_ROOT / ".claude" / "metrics" / "compliance.jsonl"
_MAX_ENTRIES = 100


def _get_file_hash(file_path: Path) -> str:
    """Get SHA256 hash of file for cache key."""
    try:
        content = file_path.read_bytes()[:4096]
        return hashlib.sha256(content).hexdigest()[:16]
    except OSError:
        return "no_file"


class SerenaSymbolCache:
    """Cache de contexto Serena lido de compliance.jsonl da sessão.

    Extrai padrões de uso de ferramentas e arquivos editados recentemente.
    """

    _memory_cache: ClassVar[dict[str, dict[str, Any]]] = {}

    @classmethod
    def _load_compliance_entries(cls, limit: int = _MAX_ENTRIES) -> list[dict[str, Any]]:
        """Load recent compliance entries from compliance.jsonl."""
        if not _COMPLIANCE_PATH.exists():
            return []
        entries: list[dict[str, Any]] = []
        try:
            lines = _COMPLIANCE_PATH.read_text(encoding="utf-8").splitlines()
            for line in lines[-limit:]:
                try:
                    entry = json.loads(line)
                    if isinstance(entry, dict):
                        entries.append(entry)
                except json.JSONDecodeError:
                    continue
        except OSError:
            pass
        return entries

    @classmethod
    def get_recent_files(cls, limit: int = 10) -> list[str]:
        """Get recently edited files from compliance log."""
        cache_key = _get_file_hash(_COMPLIANCE_PATH)
        if cache_key in cls._memory_cache:
            return cls._memory_cache[cache_key].get("files", [])

        entries = cls._load_compliance_entries()
        files: list[str] = []
        seen: set[str] = set()
        for entry in reversed(entries):
            fp = entry.get("file_path", "") or entry.get("tool_input", {}).get("file_path", "")
            if fp and fp not in seen:
                seen.add(fp)
                files.append(fp)
            if len(files) >= limit:
                break

        cls._memory_cache[cache_key] = {"files": files}
        return files

    @classmethod
    def get_tools_used(cls, limit: int = 20) -> list[str]:
        """Get recently used tools from compliance log."""
        entries = cls._load_compliance_entries()
        tools: list[str] = []
        seen: set[str] = set()
        for entry in reversed(entries[-limit:]):
            tool = entry.get("tool_name", "") or entry.get("tool", "")
            if tool and tool not in seen:
                seen.add(tool)
                tools.append(tool)
        return tools

    @classmethod
    def enrich_files(cls, files: list[str]) -> list[dict[str, Any]]:
        """Enrich file list with usage frequency data."""
        entries = cls._load_compliance_entries()
        freq: dict[str, int] = {}
        for entry in entries:
            fp = entry.get("file_path", "") or entry.get("tool_input", {}).get("file_path", "")
            if fp:
                freq[fp] = freq.get(fp, 0) + 1

        result = []
        for f in files:
            result.append({
                "path": f,
                "recent_edits": freq.get(f, 0),
                "exists": Path(f).exists(),
            })
        return result

    @classmethod
    def clear_cache(cls) -> None:
        """Clear in-memory cache."""
        cls._memory_cache.clear()


def get_cache() -> SerenaSymbolCache:
    """Factory: return SerenaSymbolCache class."""
    return SerenaSymbolCache()
